package chap_01;

public class _02_DataTypes {
    public static void main(String[] args) { // psvm or main
        System.out.println("Hello World!"); // 문자
        System.out.println("안녕하세요?"); //코드는 위에서 아래방향으로 실행됨
        System.out.println(12); //숫자
        System.out.println(-34);
        System.out.println(3.14);
        System.out.println(true); //boolean자료형
        System.out.println(false);

        System.out.println(123 + 345);
    }
}
